# Buyurtmalar ro'yxatini shakllantiruvchi dastur
def mahsulot_royxatini_ol():
    mahsulotlar = []
    print("Mahsulotlarni kiriting. To'xtash uchun 'stop' deb yozing.")
    while True:
        mahsulot = input("Mahsulot nomi: ")
        if mahsulot.lower() == 'stop':
            break
        mahsulotlar.append(mahsulot)
    return mahsulotlar

# E-bozor uchun mahsulot va narxlarini qabul qiluvchi dastur
def e_bozor_lugatini_ol():
    e_bozor = {}
    print("E-bozor uchun mahsulot va narxlarini kiriting. To'xtash uchun 'stop' deb yozing.")
    while True:
        mahsulot = input("Mahsulot nomi: ")
        if mahsulot.lower() == 'stop':
            break
        narx = input(f"{mahsulot} narxini kiriting: ")
        e_bozor[mahsulot] = narx
    return e_bozor

# E-bozor va buyurtmalarni solishtiruvchi dastur
def solishtir_buyurtmalar(buyurtma_royxati, e_bozor_lugati):
    for mahsulot in buyurtma_royxati:
        if mahsulot in e_bozor_lugati:
            print(f"{mahsulot} narxi: {e_bozor_lugati[mahsulot]}")
        else:
            print(f"Bizda bu mahsulot yo'q: {mahsulot}")

# Asosiy dastur
def asosiy_dastur():
    buyurtma_royxati = mahsulot_royxatini_ol()
    e_bozor_lugati = e_bozor_lugatini_ol()
    solishtir_buyurtmalar(buyurtma_royxati, e_bozor_lugati)

# Dastur ishga tushiriladi
asosiy_dastur()